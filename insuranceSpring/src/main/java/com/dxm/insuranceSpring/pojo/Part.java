package com.dxm.insuranceSpring.pojo;

import javax.annotation.Resource;

//车辆零件表
/**
 * 车辆零件表实体类
 * @author zf
 *
 */
public class Part {
	
	private CarType carType;
	private Integer  carPartId;	//车辆零件编号	Int
	private Integer carTypeId;	//车辆编号	Int
	private String carPartName;	//车辆零件名	varchar
	private Integer 	carPartPrice;	//市场价格	Int
	private	String	   carPartRemark;	//零件备注	varchar
	@Override
	public String toString() {
		return "Part [carPartId=" + carPartId + ", carTypeId=" + carTypeId + ", carPartName=" + carPartName
				+ ", carPartPrice=" + carPartPrice + ", carPartRemark=" + carPartRemark + "]";
	}
	public Part(int carPartId, int carTypeId, String carPartName, Integer carPartPrice, String carPartRemark) {
		super();
		this.carPartId = carPartId;
		this.carTypeId = carTypeId;
		this.carPartName = carPartName;
		this.carPartPrice = carPartPrice;
		this.carPartRemark = carPartRemark;
	}
	public Part() {
		super();
		// TODO Auto-generated constructor stub
	}
	public int getCarPartId() {
		return carPartId;
	}
	public void setCarPartId(int carPartId) {
		this.carPartId = carPartId;
	}
	public int getCarTypeId() {
		return carTypeId;
	}
	public void setCarTypeId(int carTypeId) {
		this.carTypeId = carTypeId;
	}
	public String getCarPartName() {
		return carPartName;
	}
	public void setCarPartName(String carPartName) {
		this.carPartName = carPartName;
	}
	public Integer getCarPartPrice() {
		return carPartPrice;
	}
	public void setCarPartPrice(Integer carPartPrice) {
		this.carPartPrice = carPartPrice;
	}
	public String getCarPartRemark() {
		return carPartRemark;
	}
	public void setCarPartRemark(String carPartRemark) {
		this.carPartRemark = carPartRemark;
	}
	public CarType getCarType() {
		return carType;
	}
	public void setCarType(CarType carType) {
		this.carType = carType;
	}
	
	
	
	
}
