package com.dxm.insuranceSpring.pojo;
//资料表  zf

/**
 * 资料表实体类
 * @author zf
 *
 */
public class Data {
	private int dataId;         //	资料信息编号	Int
	private String specificList;         //	清单	VARCHAR
	private String specificInvoice;         //	发票	VARCHAR
	private String remark;         //	备注	VARCHAR
	@Override
	public String toString() {
		return "t_data [dataId=" + dataId + ", specificList=" + specificList + ", specificInvoice=" + specificInvoice
				+ ", remark=" + remark + "]";
	}
	public Data(int dataId, String specificList, String specificInvoice, String remark) {
		super();
		this.dataId = dataId;
		this.specificList = specificList;
		this.specificInvoice = specificInvoice;
		this.remark = remark;
	}
	public Data() {
		super();
		// TODO Auto-generated constructor stub
	}
	public int getDataId() {
		return dataId;
	}
	public void setDataId(int dataId) {
		this.dataId = dataId;
	}
	public String getSpecificList() {
		return specificList;
	}
	public void setSpecificList(String specificList) {
		this.specificList = specificList;
	}
	public String getSpecificInvoice() {
		return specificInvoice;
	}
	public void setSpecificInvoice(String specificInvoice) {
		this.specificInvoice = specificInvoice;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}

}
