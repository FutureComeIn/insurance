package com.dxm.insuranceSpring.pojo;
         //险种类型
/**
 * 险种类型实体类
 * @author zf
 *
 */
public class InsuranceType {

	private int insuranceTypeId;	//险种编号	Int
	private String insuranceTypeName;	//险种名称	varchar
	private String insuranceTerm;	//投保年限	datetime
	private String insuranceTypeRemark;	//险种描述	varchar
	private float insuranceTypeSize;//险种系数	改成为投保金额
	private float insuranceTypePrice;	//赔损金额
	public int getInsuranceTypeId() {
		return insuranceTypeId;
	}
	public void setInsuranceTypeId(int insuranceTypeId) {
		this.insuranceTypeId = insuranceTypeId;
	}
	public String getInsuranceTypeName() {
		return insuranceTypeName;
	}
	public void setInsuranceTypeName(String insuranceTypeName) {
		this.insuranceTypeName = insuranceTypeName;
	}
	public String getInsuranceTerm() {
		return insuranceTerm;
	}
	public void setInsuranceTerm(String insuranceTerm) {
		this.insuranceTerm = insuranceTerm;
	}
	public String getInsuranceTypeRemark() {
		return insuranceTypeRemark;
	}
	public void setInsuranceTypeRemark(String insuranceTypeRemark) {
		this.insuranceTypeRemark = insuranceTypeRemark;
	}
	public float getInsuranceTypeSize() {
		return insuranceTypeSize;
	}
	public void setInsuranceTypeSize(float insuranceTypeSize) {
		this.insuranceTypeSize = insuranceTypeSize;
	}
	public float getInsuranceTypePrice() {
		return insuranceTypePrice;
	}
	public void setInsuranceTypePrice(float insuranceTypePrice) {
		this.insuranceTypePrice = insuranceTypePrice;
	}
	@Override
	public String toString() {
		return "InsuranceType [insuranceTypeId=" + insuranceTypeId + ", insuranceTypeName=" + insuranceTypeName
				+ ", insuranceTerm=" + insuranceTerm + ", insuranceTypeRemark=" + insuranceTypeRemark
				+ ", insuranceTypeSize=" + insuranceTypeSize + ", insuranceTypePrice=" + insuranceTypePrice + "]";
	}
	public InsuranceType(int insuranceTypeId, String insuranceTypeName, String insuranceTerm,
			String insuranceTypeRemark, float insuranceTypeSize, float insuranceTypePrice) {
		super();
		this.insuranceTypeId = insuranceTypeId;
		this.insuranceTypeName = insuranceTypeName;
		this.insuranceTerm = insuranceTerm;
		this.insuranceTypeRemark = insuranceTypeRemark;
		this.insuranceTypeSize = insuranceTypeSize;
		this.insuranceTypePrice = insuranceTypePrice;
	}
	public InsuranceType() {
		super();
		// TODO Auto-generated constructor stub
	}
	

}
