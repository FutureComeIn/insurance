package com.dxm.insuranceSpring.pojo;

public class NuclearDamage {
  private int nuclearDamageId;//核损编号
  private  int assessmentId;//定损编号
  private String nuclearDamageTime;//核损时间
  private int UserId;//核损人编号
  private String UserName;//核损人姓名
  private int nuclearDamageStatus;
  
  public NuclearDamage() {
	  super();
  }
  
public NuclearDamage(int nuclearDamageId, int assessmentId, String nuclearDamageTime, int userId, String userName,
		int nuclearDamageStatus) {
	super();
	this.nuclearDamageId = nuclearDamageId;
	this.assessmentId = assessmentId;
	this.nuclearDamageTime = nuclearDamageTime;
	UserId = userId;
	UserName = userName;
	this.nuclearDamageStatus = nuclearDamageStatus;
}
public int getNuclearDamageId() {
	return nuclearDamageId;
}
public void setNuclearDamageId(int nuclearDamageId) {
	this.nuclearDamageId = nuclearDamageId;
}
public int getAssessmentId() {
	return assessmentId;
}
public void setAssessmentId(int assessmentId) {
	this.assessmentId = assessmentId;
}
public String getNuclearDamageTime() {
	return nuclearDamageTime;
}
public void setNuclearDamageTime(String nuclearDamageTime) {
	this.nuclearDamageTime = nuclearDamageTime;
}
public int getUserId() {
	return UserId;
}
public void setUserId(int userId) {
	UserId = userId;
}
public String getUserName() {
	return UserName;
}
public void setUserName(String userName) {
	UserName = userName;
}
public Integer getNuclearDamageStatus() {
	return nuclearDamageStatus;
}
public void setNuclearDamageStatus(Integer nuclearDamageStatus) {
	this.nuclearDamageStatus = nuclearDamageStatus;
}
@Override
public String toString() {
	return "NuclearDamage [nuclearDamageId=" + nuclearDamageId + ", assessmentId=" + assessmentId
			+ ", nuclearDamageTime=" + nuclearDamageTime + ", UserId=" + UserId + ", UserName=" + UserName
			+ ", nuclearDamageStatus=" + nuclearDamageStatus + "]";
}
	
}
